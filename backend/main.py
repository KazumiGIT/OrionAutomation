"""
Orion Automation — Blog API (FastAPI)
=====================================
Public read API + admin CRUD + image upload/proxy, backed by Google Drive.

Run locally:
  cd OrionAutomation
  uvicorn backend.main:app --reload --port 8000

Public endpoints:
  GET  /api/blogs                       list published posts
  GET  /api/blogs/{slug}                fetch one published post
  GET  /api/images/{file_id}            proxy a Drive image (no auth)

Admin endpoints (Authorization: Bearer <ADMIN_PASSWORD>):
  POST   /api/admin/login               verify username + password
  GET    /api/admin/blogs               list all posts (incl. drafts)
  GET    /api/admin/blogs/{slug}        fetch any post by slug
  POST   /api/admin/blogs               create or upsert a post
  PUT    /api/admin/blogs/{slug}        update a post
  DELETE /api/admin/blogs/{slug}        delete a post
  POST   /api/admin/images              upload an image (multipart/form-data)
  GET    /api/admin/images              list recent images
  GET    /api/admin/mcp-key             fetch current MCP API key (admin only)
  POST   /api/admin/mcp-key/rotate      generate a new MCP API key

MCP server (mounted at /mcp). Auth: Authorization: Bearer <MCP key>, where
the key is generated by the admin in the CMS. The MCP endpoint is unusable
until the admin clicks "Generate" once.
"""

