@app.get("/api/images/{file_id}")
def get_image(file_id: str):
    """Proxy a Drive image. Public — anyone with the URL can view."""
    try:
        data, mime = get_storage().download_image(file_id)
    except Exception as e:
        raise HTTPException(status_code=404, detail=f"Image not available: {e}")
    return Response(
        content=data,
        media_type=mime,
        headers={"Cache-Control": "public, max-age=86400"},
    )


@app.post("/api/chat")
def chat(
    body: ChatIn,
    request: Request,
    authorization: str | None = Header(default=None),
):
    """Public chatbot proxy. The Gemini key and guardrails live server-side.

    Persona/knowledge-base overrides in the body are applied only for admins
    (used by the CMS live preview); guests always get the locked config.
    """
    client_ip = request.client.host if request.client else "unknown"
    _rate_limit_chat(client_ip)

    overrides = None
    if is_admin_token(authorization):
        overrides = {
            "botName": body.botName,
            "aiGoal": body.aiGoal,
            "personality": body.personality,
            "knowledgeBase": body.knowledgeBase,
        }

    user_ctx = None
    if isinstance(body.user, dict) and body.user.get("username"):
        user_ctx = {
            "username": str(body.user.get("username"))[:80],
            "plan": str(body.user.get("plan"))[:120] if body.user.get("plan") else None,
        }

    history = [{"role": t.role, "text": t.text} for t in body.history]

    try:
        reply = chatbot.generate_reply(
            message=body.message,
            history=history,
            user_ctx=user_ctx,
            overrides=overrides,
        )
    except RuntimeError as exc:
        raise HTTPException(status_code=503, detail=str(exc))

    return {"reply": reply}


# ─── Admin routes ─────────────────────────────────────────────────────────────

@app.post("/api/admin/login")
def admin_login(body: LoginIn):
    if not verify_admin_login(body.username, body.password):
        raise HTTPException(status_code=401, detail="Invalid username or password")
    # The bearer token IS the password — single-admin model. Frontend stores it
    # and sends it as Bearer on subsequent calls.
    return {"ok": True, "token": body.password, "username": body.username}


@app.get("/api/admin/blogs", dependencies=[Depends(require_admin)])
def admin_list_blogs():
    posts = get_storage().list_posts(include_drafts=True)
    return {"posts": posts, "total": len(posts)}


@app.get("/api/admin/blogs/{slug}", dependencies=[Depends(require_admin)])
def admin_get_blog(slug: str):
    post = get_storage().get_post(slug, include_drafts=True)
    if not post:
        raise HTTPException(status_code=404, detail="Post not found")
    return post


@app.post(
    "/api/admin/blogs",
    dependencies=[Depends(require_admin)],
    status_code=status.HTTP_201_CREATED,
)
def admin_create_blog(post: PostIn):
    data = _normalize_post(post.model_dump(exclude_none=False))
    saved = get_storage().save_post(data)
    return saved


@app.put("/api/admin/blogs/{slug}", dependencies=[Depends(require_admin)])
def admin_update_blog(slug: str, post: PostIn):
    data = post.model_dump(exclude_none=False)
    data["slug"] = slug
    data["updatedAt"] = date.today().isoformat()
    data = _normalize_post(data)
    saved = get_storage().save_post(data)
    return saved


@app.delete("/api/admin/blogs/{slug}", dependencies=[Depends(require_admin)])
def admin_delete_blog(slug: str):
    ok = get_storage().delete_post(slug)
    if not ok:
        raise HTTPException(status_code=404, detail="Post not found")
    return {"ok": True}


@app.post("/api/admin/images", dependencies=[Depends(require_admin)])
async def admin_upload_image(file: UploadFile = File(...)):
    if not file.content_type or not file.content_type.startswith("image/"):
        raise HTTPException(status_code=400, detail="File must be an image")
    content = await file.read()
    if len(content) > 10 * 1024 * 1024:
        raise HTTPException(status_code=413, detail="Image must be under 10 MB")
    safe_name = re.sub(r"[^A-Za-z0-9._-]", "_", file.filename or "image")
    info = get_storage().upload_image(
        filename=safe_name, content=content, mime=file.content_type
    )
    return {
        "id": info["id"],
        "name": info["name"],
        "mime": info["mime"],
        "url": f"/api/images/{info['id']}",
    }


@app.get("/api/admin/images", dependencies=[Depends(require_admin)])
def admin_list_images(limit: int = 50):
    images = get_storage().list_images(limit=limit)
    return {
        "images": [
            {**img, "url": f"/api/images/{img['id']}"} for img in images
        ]
    }


