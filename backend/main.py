@app.get("/api/admin/mcp-key", dependencies=[Depends(require_admin)])
def admin_get_mcp_key():
    """Return the current MCP API key and its metadata, or null if not generated yet."""
    meta = keys.get_metadata()
    return meta or {"key": None, "createdAt": None, "rotatedAt": None}


@app.post("/api/admin/mcp-key/rotate", dependencies=[Depends(require_admin)])
def admin_rotate_mcp_key():
    """Generate a brand-new MCP API key. Any previously-stored key stops working."""
    return keys.rotate()


